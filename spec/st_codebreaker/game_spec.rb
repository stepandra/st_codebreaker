      allow_any_instance_of(described_class).to receive(:rand).and_return(1, 2, 1, 2)
    end

    describe '#initialize' do
      let(:secret_code) { subject.instance_variable_get(:@secret) }

      it 'sets attempts count to 10' do
        expect(subject.instance_variable_get(:@attempts)).to eq(10)
      end

      it 'init no_hint as false' do
        expect(subject.instance_variable_get(:@no_hint)).to eq(false)
      end

      it 'saves 4 numbers secret code' do
        expect(secret_code.size).to eq(4)
      end

      it 'saves secret code with numbers from 1 to 6' do
        expect(secret_code).to match(/[1-6]{4}/)
      end

      it 'generates uniq secret code for different games' do
        another_game = Game.new
        code_for_another_game = another_game.instance_variable_get(:@secret_code)
        expect(secret_code).not_to eq(code_for_another_game)
      end
    end

    describe '#compare' do
      it 'returns "" if no match' do
        expect(subject.compare('3333')).to eq('')
      end

      it 'decrease available attempts by 1' do
        expect { subject.compare('3333') }.to change { subject.instance_variable_get(:@attempts) }.by -1
      end

      it 'validate user guess' do
        expect(subject.compare('11111')).to eq('Input error')
        expect(subject.compare('111')).to eq('Input error')
      end
    end

    describe '#hint' do
      it 'shows correct hint' do
        secret_code = subject.instance_variable_get(:@secret)
        regexp = Regexp.new("[#{secret_code.chars.join('|')}]{1}")
        expect(subject.hint).to match(regexp)
      end
    end

    describe '#save' do
      it 'sends information when user enter his name' do
        allow(File).to receive(:open)
        expect(File).to receive(:open).with('../../stats.txt', 'a+')
        subject.save('Stefan')
      end
